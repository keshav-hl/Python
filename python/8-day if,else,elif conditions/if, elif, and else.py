#if statement/
time = 20  # 20 represents 8 PM in 24-hour format
if time == 20:
    print("It's time for dinner!")

#elif statement/
time = 18  # 6 PM
if time == 20:
    print("It's time for dinner!")
else:
    print("It's not dinner time yet.")

#else statements/
time = 15  # 3 PM

if time == 8:
    print("It's breakfast time!")
elif time == 13:
    print("It's lunch time!")
elif time == 20:
    print("It's dinner time!")
else:
    print("It's not a meal time.")

    