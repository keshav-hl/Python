# comparison operator/

a = 10
b = 20

#Checking a is equal to b/
print(a == b)  

#Checking a is not equal to b/
print(a != b) 

#Checking a is greater than b/
print(a > b) 

#Checking a is less than b/
print(a < b)

#Checking a is greater than and equal to b/
print(a >= 10) 

#Checking a is lesser than and equal to b/
print(b <= 25)  